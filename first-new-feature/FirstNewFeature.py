def addTwoNumbers(a: int, b: int) -> int:
    return a + b

def multiplyTwoNumbers(a: int, b: int) -> int:
    return a * b

def subtractTwoNumbers(a: int, b: int) -> int:
    return a - b

def divideTwoNumbers(a: int, b: int) -> int:
    return a / b

def powerOfTwo(a: int) -> int:
    return a ** 2

def squareRoot(a: int) -> int:
    return a ** 0.5

def cubeRoot(a: int) -> int:
    return a ** (1/3)

